package ex_03_Operators;

public class Lab021_Arithmetic_Operators {

    public static void main(String[] args) {
//        Arithmetic Operators

        int a = 20;
        int b = 3;
        float c = 3f;

        System.out.println(a+b);
        System.out.println(a-b);
        System.out.println(a*b);
        System.out.println(a/b);
        System.out.println(a/c);

    }
}
