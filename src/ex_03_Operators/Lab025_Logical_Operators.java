package ex_03_Operators;

public class Lab025_Logical_Operators {

    public static void main(String[] args) {

        boolean a = true;
        System.out.println(a);
        System.out.println(!a);
        System.out.println(!!a);

        boolean  b= true;
        System.out.println(b);
        System.out.println(!b);
        System.out.println(!!b);

        boolean c = true || false; // || -> or operator
        System.out.println(c);
    }
}
